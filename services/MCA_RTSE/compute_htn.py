import numpy as np
from numba import njit, float64

# custom imports
import params


def calculateGpi(htn):
    """
    Calculate the generated power for each grid cell.

    This function computes the power output for each grid cell by multiplying
    the given hourly thermal energy (HTN) with constants representing panel
    size, panel energy efficiency, and grid cell area.
    """
    return htn * params.PANEL_SIZE * params.PANEL_ENERGY_EFF * params.GRIDCELL_AREA


def calculateGpiPoly(gti_grid, time_and_day_arr, latitude, tilt_roof, ori_roof, valid_mask):
    """Returns layout for every timestep of energy generated by each valid gridcell"""
    time_arr = time_and_day_arr.timeArr
    day_arr = time_and_day_arr.dayArr
    poly_shape = gti_grid.shape
    gti_gridcellwise = gti_grid.reshape((time_arr.shape[0], -1))

    htn_gridcellwise = computeHtnForEachGridHour(day_arr, time_arr, latitude, tilt_roof, ori_roof, gti_gridcellwise)
    htn_grid = htn_gridcellwise.reshape(poly_shape)
    gpi_grid = calculateGpi(htn_grid)
    return gpi_grid


def calculateGpiPolyF(gti_grid, time_and_day_arr, latitude, tilt_roof, ori_roof, valid_mask):
    """Same as calculateGpiPoly, but avoids running computeHtnForEachGridHour for
    invalid panel positions"""
    time_arr = time_and_day_arr.timeArr
    day_arr = time_and_day_arr.dayArr
    valid_mask1 = valid_mask == 0
    c_gti_grid = np.moveaxis(gti_grid, 0, 2)
    valid_gti_gridcellwise = c_gti_grid[valid_mask1].T

    valid_htn_gridcellwise = computeHtnForEachGridHour(day_arr, time_arr, latitude, tilt_roof,
                                                       ori_roof, valid_gti_gridcellwise)
    htn_grid = np.zeros_like(c_gti_grid)
    htn_grid[valid_mask1] = valid_htn_gridcellwise.T
    ahtn_grid = np.moveaxis(htn_grid, 2, 0)
    gpi_grid = calculateGpi(ahtn_grid)
    return gpi_grid


def calculateCUF(generation, numberOfPanels):
    """
        Calculate the Capacity Utilization Factor (CUF) for a solar panel system.

        The CUF is determined by dividing the actual energy generation by the
        maximum possible energy generation over a year, given the number of panels
        and their wattage.

    """
    return generation / (params.PANEL_WATTAGE * 8760 * numberOfPanels)


@njit([(float64[:, :], float64, float64)], cache=True)
def boundingFuncA(arr, lowerb, upperb):
    """
        Constrain the values in a 2D array to lie within specified bounds.
    """
    for i in range(arr.shape[0]):
        for j in range(arr.shape[1]):
            if arr[i, j] > upperb:
                arr[i, j] = upperb
            elif arr[i, j] < lowerb:
                arr[i, j] = lowerb


@njit([(float64[:], float64, float64)], cache=True)
def boundingFuncB(arr, lowerb, upperb):
    """
     Constrain the values in a 1D array to lie within specified bounds.
    """
    for i in range(arr.shape[0]):
        if arr[i] > upperb:
            arr[i] = upperb
        elif arr[i] < lowerb:
            arr[i] = lowerb


@njit([(float64[:, :], float64[:, :], float64[:])], cache=True)
def processDiffuseRadiation(darr, carr, cl_index_poly_coeffs):
    """
    Calculate the diffuse radiation ratio for each element in the input arrays.

    This function processes the clearness index array `carr` using polynomial
    coefficients `cl_index_poly_coeffs` to compute the diffuse radiation ratio,
    which is stored in the output array `darr`. The calculation is based on
    different conditions of the clearness index values.

    """
    for i in range(carr.shape[0]):
        for j in range(carr.shape[1]):
            if carr[i, j] >= 0.8:
                darr[i, j] = 0.165
            elif carr[i, j] < 0.22:
                darr[i, j] = 1 - 0.009 * carr[i, j]
            else:
                acc = 0
                term = 1
                val = carr[i, j]
                for inx in range(len(cl_index_poly_coeffs)):
                    cli = cl_index_poly_coeffs[inx]
                    term *= val
                    acc += cli * term
                darr[i, j] = acc


